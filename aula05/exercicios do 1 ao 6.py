# dados a lista L=[5, 7, 9, 4, 1, 3], exiba
l = [5, 7, 9, 4, 1, 3]
# 1- tamanho da lista
print(len(l))
# 2- o maior valor da lista
print(max(l))
# 3- o menor valor da lista
print(min(l))
# 4- a soma dos valores
print(sum(l))
# 5- os elementos em ordem crescente
print(sorted(l))
# 6- os elementos em ordem decrescente
print(sorted(l, reverse= True))